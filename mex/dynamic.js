// Dynamic programming is a programming technique used to solve problems by 
// breaking them down into smaller subproblems and solving each subproblem 
// only once, storing the results of subproblems to avoid redundant computation.

// Dynamic programming is often used for optimization problems, where 
// the goal is to find the best solution among many possible solutions. 
// By breaking the problem down into smaller subproblems and solving them 
// efficiently, dynamic programming can often find the best solution in a reasonable amount of time.

// The basic steps of dynamic programming are:

// Define the problem in terms of smaller subproblems.
// Define a recursive function to solve each subproblem.
// Use memoization or tabulation to store the results of subproblems 
// and avoid redundant computation.
// Combine the solutions to subproblems to solve the original problem.
// Dynamic programming can be used to solve many types of problems, including
//  shortest path problems, knapsack problems, and string matching problems,
//   among others.

