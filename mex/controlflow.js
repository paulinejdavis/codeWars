// Conditional statements:

// Conditional statements are used to control the flow of execution in a program based on a condition.
// In JavaScript, the if statement is used to execute code only if a condition is true.
// The if statement can be combined with the else if and else statements to create a chain of conditions 
// that execute different code depending on the value of a variable or expression.
// Conditional operators, such as the ternary operator (condition ? true_value : false_value), can be used
//  to write more concise conditional statements.
// Sorting algorithms:

// Sorting algorithms are used to arrange a collection of elements in a specific order.
// The most common sorting algorithms are comparison-based algorithms, which compare pairs of 
// elements and swap them if they are in the wrong order.
// Bubble sort is a simple sorting algorithm that repeatedly swaps adjacent elements that are 
// in the wrong order until the list is sorted.
// Selection sort is another simple sorting algorithm that repeatedly selects the smallest 
// remaining element and swaps it with the current element until the list is sorted.
// Insertion sort is a sorting algorithm that builds the final sorted list one item at a 
// time by inserting each item into its proper position.
// Quick sort is a popular sorting algorithm that uses a divide-and-conquer approach to 
// sort a list by partitioning it into smaller sub-lists, sorting those sub-lists recursively, 
// and then combining them back together.
// Merge sort is another popular sorting algorithm that uses a divide-and-conquer approach to 
// sort a list by splitting it into smaller sub-lists, sorting those sub-lists recursively, 
// and then merging them back together.